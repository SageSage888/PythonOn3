use project_DB;

CREATE TABLE student_groups(
    group_id INT PRIMARY KEY AUTO_INCREMENT,
    group_name VARCHAR(10));

CREATE TABLE students(
    student_id INT PRIMARY KEY AUTO_INCREMENT,
    first_name VARCHAR(20),
    last_name VARCHAR(20),
    group_id INT,
    FOREIGN KEY(group_id) REFERENCES student_groups(group_id)
    ON DELETE SET NULL
    ON UPDATE CASCADE);
CREATE TABLE subjects(
    subject_id INT PRIMARY KEY AUTO_INCREMENT,
    subject_name VARCHAR(20),
    teacher VARCHAR(30));
CREATE TABLE rating(
    rating_id INT PRIMARY KEY AUTO_INCREMENT,
    student_id INT,
    subject_id INT,
    rating_value INT,
    rating_date DATE DEFAULT (CURRENT_DATE),
    FOREIGN KEY(student_id) REFERENCES students(student_id)
    ON DELETE SET NULL
    ON UPDATE CASCADE,
    FOREIGN KEY(subject_id) REFERENCES subjects(subject_id)
    ON DELETE SET NULL
    ON UPDATE CASCADE
);
CREATE TABLE subjects_in_groups(
    subject_id INT,
    group_id INT,
    FOREIGN KEY(subject_ID) REFERENCES subjects(subject_ID) ON DELETE SET NULL ON UPDATE CASCADE,
    FOREIGN KEY(group_ID) REFERENCES student_groups(group_ID) ON DELETE SET NULL ON UPDATE CASCADE
);

